<p>彩票程序后台系统. </p>
<p>本套程序我们依托于PHP--Laravel框架。<br>
    在后台管理系统中主要模块包括：<br>
    1，日志管理，（展示网站日志信息)。<br>
    2，文章管理（由管理员进行发布相关文章，展示给网站用户）。<br>
    3，网站概要。（主要展示网站的一些基本信息，如：网站会员总数，等）。<br>
    4，会员管理。（主要针对本网站会员进行相关操作，例如：为会员设置相应权限等）。<br>
    5，权限（组）/角色（组）管理（由网站管理员对本站会员分配相应权限，对权限或者角色进行分组）。
</p>
